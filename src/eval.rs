fn mirror_index(index: usize) -> usize {
    63 - index
}

fn get_piece_by_type(board: &Board, piece: Piece, color: Color) -> i32 {
    let piece_bitboard = board.pieces(piece);
    let color_bitboard = board.color_combined(color);
    let pieces_to_evaluate = *piece_bitboard & *color_bitboard;
    let mut score: i32= 0;

    for square in pieces_to_evaluate {
        let index = if color == Color::White {
            square.to_index()
        } else {
            mirror_index(square.to_index())
        };

        score += match piece {
            Piece::Pawn => 100 + PAWN_PST[index],
            Piece::Bishop => 330 + BISHOP_PST[index],
            Piece::Knight => 320 + KNIGHT_PST[index], 
            Piece::Rook => 500 + ROOK_PST[index],
            Piece::Queen => 900 + QUEEN_PST[index],
            Piece::King => KING_MIDDLEGAME_PST[index],
        };
    }
    score
}

/// Very naive utility function
pub(crate) fn eval(board: &Board) -> i32 {
    let mut utility_score: i32 = 0;
    let player = board.side_to_move();
    for piece in [Piece::Pawn, Piece::Bishop, Piece::Knight, Piece::Rook, Piece::Queen, Piece::King] {
        utility_score += get_piece_by_type(board, piece, player);
        utility_score -= get_piece_by_type(board, piece, !player);
    }
    utility_score
}
